#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	index;

	index = 0;
	while (src[index] != '\0')
	{
		dest[index] = src[index];
		index++;
	}
	dest[index] = '\0';
	return (dest);
}

int	main(void)
{
	char	dest[] = "adem";
	char	src[] = "ahmetcim";

	printf("%s \n", ft_strcpy(dest,src));

}
